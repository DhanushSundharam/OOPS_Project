package RestrauntDeliveryApp;

public class Restaurant {

  private int id ;
  private Address address ;
  private Menu menu ;
  private static int numOfRestaurants;
  private final String name ;
  private int ratings ;

  public Restaurant (Address address , Menu menu , String name , int ratings){
    this.address = address ;
    this.numOfRestaurants = id ;
    this.id = id ;
    this.menu = menu ;
    this.name = name ;
    this.ratings = ratings ;
  }

  public String getName (){
    return this.name ;
  }



}
